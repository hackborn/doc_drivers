func (m *makeTemplates) fmtMapped() string {
	var sb strings.Builder
	for k, _ := range m.mapped {
		if sb.Len() > 0 {
			sb.WriteString(", ")
		}
		sb.WriteString(k)
	}
	return sb.String()
}

type makeProcessFunc func(c *makeContent) error

type makeContent struct {
	registry.Content
	b strings.Builder
}

func newMakeContent(name, content string) *makeContent {
	ic := &makeContent{}
	ic.Name = name
	ic.b.Grow(len(content) * 2)
	scanner := bufio.NewScanner(strings.NewReader(content))
	scanner.Split(bufio.ScanLines)
	for scanner.Scan() {
		ic.Lines = append(ic.Lines, scanner.Text())
	}
	return ic
}

